#include "ft_printf.h"

int	ft_putstrr(char *s)
{
	int	n;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	n = ft_strlenn(s);
	write (1, s, n);
	return (n);
}
